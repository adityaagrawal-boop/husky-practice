export function add(a, b) {
  return a + b;
}

export function multiply(a, b) {
  return a * b;
}

export function subtract(a, b) {
  return a - b;
}

export function applyDiscount(price, percent) {
  return price - (price * percent) / 100;
}

export function isEven(n) {
  return n % 2 === 0;
}
